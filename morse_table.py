def dot_dash(text):
    seq = []
    for c in text.upper():
        if c in morse:
            seq.append(morse[c])
    return " ".join(seq)


def seq(text):
    seq = [0] * 10
    for c in dot_dash(text):
        if c == ".":
            seq.extend([1, 0])
        elif c == "-":
            seq.extend([1, 1, 1, 0])
        elif c == " ":
            seq.extend([0, 0])
    seq.extend([0] * 9)
    return seq


